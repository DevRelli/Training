/* REST HTTP Verbs
    Get -> Retornar e receber dados de um servidor (links)
    Post -> Cria um novo produto (item)
    Patch -> Alterar (Atualizar) parcialmente um dado existente (item)
    Put ->  Alterar (Substituir) completamente um dado (item)
    Delete -> Deleta um item

   STATUS CODE (Requests)
   pesquisar: lista de códigos estado http (wikipedia)
   exemplos:
    200 -> Conexão feita com sucesso
    201 -> Algo foi criado com sucesso
    404 -> Status Não encontrado

   API -> Interface de Programação de Aplicações (Biblioteca, JSON, Interface, Aplicação)

    WebServices - apis para se comunicar pela web

    API do Youtube - ex: requests para poder fazer listagem de vídeos (comunicação entre aplicações e interfaces)
/* 
    API REST vs API RESTFul (padrão arquitetural)

      REST: (padrão dev de web service)

        1. Cliente Servidor
        2. Stateless 
        3. Cache (economizar dados de processamento)
        4. Camadas
        5. Interface uniforme

    RESTFul: REST + especificações

        Dependendo dos níveis(0,1,2,3), uma API REST se torna uma API RESTFul, que seria uma REST porém mais completa.

 */
